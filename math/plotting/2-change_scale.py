#!/usr/bin/env python3
"""
2-change_scale.py
Plots the exponential decay of Carbon-14 over time.
"""

import numpy as np
import matplotlib.pyplot as plt


def change_scale():
    """
    Displays a line graph of the fraction of Carbon-14 remaining
    over time with a logarithmic y-axis.
    """
    x = np.arange(0, 28651, 5730)
    r = np.log(0.5)
    t = 5730
    y = np.exp((r / t) * x)
    plt.figure(figsize=(6.4, 4.8))

    plt.plot(x, y)
    plt.yscale("log")
    plt.xlim(0, 28650)
    plt.xlabel("Time (years)")
    plt.ylabel("Fraction Remaining")
    plt.title("Exponential Decay of C-14")

    plt.show()
